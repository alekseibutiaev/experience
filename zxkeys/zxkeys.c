void fill_keyboards(uint8_t (*keboard)[256], const uint8_t(*keys)[6]) {
  for(int i = 0; i < sizeof(*keboard); ++i) {
    uint8_t val = 0xFF;
    for(uint8_t j = 0; j < sizeof(*keys) && (*keys)[j]; ++j) {
      const key_layer_t* kl = layers + (*keys)[j];
      if(0 == (i & ~kl->top))
        val &= kl->tbit;
    }
    (*keboard)[i] = val;
  }
}

