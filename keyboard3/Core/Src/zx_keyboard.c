void build_registr(uint8_t (*regs)[REG_COUNT], const uint8_t(*keys)[MAX_KEY]) {
  uint8_t idx;
  memset(*regs, 0xFF, REG_COUNT);
  if(0 != (idx = (*keys)[0]))
    (*regs)[layers[idx].treg] &= layers[idx].tbit;
  else
    return;
  if(0 != (idx = (*keys)[1]))
    (*regs)[layers[idx].treg] &= layers[idx].tbit;
  else
    return;
  if(0 != (idx = (*keys)[2]))
    (*regs)[layers[idx].treg] &= layers[idx].tbit;
  else
    return;
  if(0 != (idx = (*keys)[3]))
    (*regs)[layers[idx].treg] &= layers[idx].tbit;
  else
    return;
  if(0 != (idx = (*keys)[4]))
    (*regs)[layers[idx].treg] &= layers[idx].tbit;
  else
    return;
  if(0 != (idx = (*keys)[5]))
    (*regs)[layers[idx].treg] &= layers[idx].tbit;
  else
    return;
}

uint8_t assemply_data(const uint8_t(*regs)[REG_COUNT], const uint8_t addr) {
  uint8_t res = 0xFF;
  if(addr & 0x01)
    res &= (*regs)[0];
  if(addr & 0x02)
    res &= (*regs)[1];
  if(addr & 0x04)
    res &= (*regs)[2];
  if(addr & 0x08)
    res &= (*regs)[3];
  if(addr & 0x10)
    res &= (*regs)[4];
  if(addr & 0x20)
    res &= (*regs)[5];
  if(addr & 0x40)
    res &= (*regs)[6];
  if(addr & 0x80)
    res &= (*regs)[7];
  return res;
}

void build_key_table(uint8_t(*table)[256], const uint8_t(*keys)[6]) {
  uint8_t regs[8] = {0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF};
  build_registr(&regs, keys);
  for(int i = 0; i < sizeof(*table); ++i)
    (*table)[i] = assemply_data(&regs, ~(uint8_t)i);
  return;
}

