int luhn_16(unsigned long long value)
{
  int data[2][16] = {
    {0x0,0x1,0x2,0x3,0x4,0x5,0x6,0x7,0x8,0x9,0xa,0xb,0xc,0xd,0xe,0xf},
    {0x0,0x2,0x4,0x6,0x8,0xa,0xc,0xe,0x1,0x3,0x5,0x7,0x9,0xb,0xd,0xf}
  };
  int sum = 0;
  int index = 0;
  while(value){
    sum += data[++index % 2][value % 16];
    value /= 16;
  }
  return 16 - sum % 16;
}



